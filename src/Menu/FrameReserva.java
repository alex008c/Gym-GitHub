    private void campoidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoidKeyReleased
        int cod;
        boolean encontrado=false;

        cod=Integer.parseInt(campoid.getText());

        Scanner s;
        try {
            File f=new File("Reserva.txt");
            
            if(!f.exists())
            {
                f.createNewFile();
                estatus.setText("Creando...");
            }
            s = new Scanner(f);
                while (s.hasNextLine() && !encontrado)
                {
                    String linea = s.nextLine();

                    Scanner sl = new Scanner(linea);

                    sl.useDelimiter("\\s*;\\s*");
                    try {
                        if(cod==Integer.parseInt(sl.next()))
                        {
                            campoidsala.setText(sl.next());
                            campoidclientere.setText(sl.next());
                            campodia.setText(sl.next());
                            campomes.setText(sl.next());
                            campoyear.setText(sl.next());
                            campoidhorareserva.setText(sl.next());
                            campoidestadoreserva.setText(sl.next());
                        
                            encontrado=true;
                            crear = true;
                            estatus.setText("Modificando...");
                        }
                        else
                        { 
                            campoidsala.setText("");
                            campoidclientere.setText("");
                            campodia.setText("");
                            campomes.setText("");
                            campoyear.setText("");
                            campoidhorareserva.setText("");
                            campoidestadoreserva.setText("");
                            estatus.setText(""); 
                            encontrado=false;
                            crear = false;
                            estatus.setText("Creando...");
                        }
                        
                    } // fin try
                    catch (Exception  e1)
                    {
                       JOptionPane.showMessageDialog(null,"Error al leer Archivo " + e1);
                       
                    }
                } // fin while
            

            s.close();
        } // fin try
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error al leer Archivo " + e);
        }
       
    }//GEN-LAST:event_campoidKeyReleased

