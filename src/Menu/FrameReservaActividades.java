                            campodia.setText(sl.next());
                            campomes.setText(sl.next());
                            campoyear.setText(sl.next());
                            campoidestado.setText(sl.next());
                            campoidcli.setText(sl.next());
                            campoidacti.setText(sl.next());
                            campoidhoraacti.setText(sl.next());
